package 반북문;

public class 반복문2 {

	public static void main(String[] args) {
			
		// while loop condition.
		
		int i = 0; // 시작값 
		
		while (i < 10) { // 조건식
			System.out.println("밥 먹을 시간이예요");
			i++;// 증감
		}
			System.out.println(i);
		
			// 반복문엔 시작값 , 조건식 , 증감식이 필요하다.
	
			
	
	
	
	
	
	
	
	
	
	
	
	} //main 

} //class 
